// : 순서가 있는 요소들의 집합, 여러개의 데이터가 모인 리스트

// 배열 생성 방법
// 1. 생성자 함수
// 2. 리터럴

// 생성자 함수
let arr1 = new Array(1, 2, 3);
let arr2 = new Array(3); // 길이가 3인 비어있는 배열
console.log(arr1); // [1, 2, 3]
console.log(arr2); // [비어 있음 × 3]

// 리터럴
let arr3 = [1, 2, 3];
let arr4 = [3];
console.log(arr3); // [1, 2, 3]
console.log(arr4); // [3]
